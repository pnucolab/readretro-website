<Heading tag="h2" class="mt-5 mb-2">Pathway Explorer</Heading>
<Heading tag="h5" class="font-light mb-5">A webtool for retrosynthetic planning of natural prodcuts</Heading>
<P class="mb-5"
    >READRetro provides feasible biosynthetic pathways for target molecules (i.e. natural prodcuts). A target molecule should be entered in SMILES format.
    You may found the SMILES representation of your target molecule in web databases such as <A  href="https://pubchem.ncbi.nlm.nih.gov/">PubChem</A></P>
<P class="mb-5">
    Details in READRetro coud be found in paper.
    Citation info: Lee and Kim et al., READRetro: Natural Product Biosynthesis Planning with Retrieval-Augmented Dual-View Retrosynthesis. BioRxiv, 2023
</P>
<P class="mb-10">
    If you want to optimize or modify the model, you may download in your local environment in open source repository.
    Code available: READRetro 
